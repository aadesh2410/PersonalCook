/** Return dishes filtered by region and optional vegType */
export function getDishesByRegion(
  region: string,
  vegType?: string
): FoodItem[] {
  return REGIONAL_DISHES.filter((dish) => {
    const regionMatch = dish.region === region;
    if (!regionMatch) return false;
    if (!vegType) return true;
    if (vegType === 'veg') return dish.vegType === 'veg';
    if (vegType === 'eggetarian') return dish.vegType === 'veg' || dish.vegType === 'eggetarian';
    return true; // non-veg: all dishes
  });
}

/** Get dishes filtered by region, vegType, mealType, and optional spice level */
export function getFilteredDishes(
  region: string,
  vegType: string,
  mealType: string,
  spiceLevel?: string
): FoodItem[] {
  return REGIONAL_DISHES.filter((dish) => {
    const regionMatch = dish.region === region;
    if (!regionMatch) return false;

    const mealTypeMatch = dish.mealType.includes(mealType as any);
    if (!mealTypeMatch) return false;

    if (vegType === 'veg' && dish.vegType !== 'veg') return false;
    if (vegType === 'eggetarian' && dish.vegType === 'non-veg') return false;

    if (spiceLevel && dish.spiceLevel && dish.spiceLevel !== spiceLevel) return false;

    return true;
  });
}
